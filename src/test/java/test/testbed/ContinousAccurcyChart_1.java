	private double calculate(double[] ideal, double[] actual, List<Double> ampes){
		double MAPE = 0.0;
		double total = 0.0;
		
		double yMean = 0.0;
		double RSS = 0.0;
		double Ry = 0.0;
		double Pd = 0.0;
		for (int i = 0; i < ideal.length; i++) {
			yMean += ideal[i];
		}
		yMean = yMean/ideal.length;
		for (int j = 0; j < ideal.length; j++) {
			MAPE = 0.0;
			RSS += Math.pow((ideal[j] - actual[j]),2);
			  if ((actual[j] > 0 && ideal[j] < 0) ||
					   (actual[j] < 0 && ideal[j] > 0)) {
				   MAPE = Math.abs((ideal[j] + actual[j]) / 
						   (ideal[j] - actual[j]));
				 
				 
			   } else {
				   MAPE = Math.abs((ideal[j] - actual[j]) / 
						   (ideal[j]+ actual[j]));
			   }
			  Pd +=  Math.abs((ideal[j] - yMean) / 
					   (yMean));
			  Ry += Math.pow((ideal[j] - yMean),2);
			  total += MAPE;
			  ampes.add(MAPE);  
		}
		Collections.sort(ampes);
		
		System.out.print("PD:" + Pd/ampes.size()  + "\n");
		System.out.print("RS:" + (1-(RSS/Ry))  + "\n");
		return total;
	}

